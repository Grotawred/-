function change() {
    const el = document.getElementsByClassName("infoStud");
    for(var x = 0; x < el.length;x++){
        el[x].style.visibility="visible";
    }
}

function changeBack() {
    const el = document.getElementsByClassName("infoStud");
    for(var x = 0; x < el.length;x++){
        el[x].style.visibility="hidden";
    }
}

